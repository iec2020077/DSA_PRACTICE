class Solution
{
    public:
    //Function to find the nodes that are common in both BST. 
    void inorder(vector<int> &in, Node* root)
    {
        if(root == NULL) return;
        
        inorder(in, root->left);
        
        in.push_back(root->data);
        
        inorder(in,root->right);
    }
    vector <int> findCommon(Node *root1, Node *root2)
    {
       vector<int> v1;
       inorder(v1,root1);
       
       
       vector<int> v2;
       inorder(v2,root2);
       
       set<int> s;
       for(auto it:v1)
       s.insert(it);
       
       vector<int> ans;
       for(auto it:v2)
       {
           if(s.find(it) != s.end())
           ans.push_back (it);
           
       }
       
       
    //   for(auto it:s)
    //   ans.push_back(it);
       
       return ans;
    }
};




